import numpy as np
from keras.models import Sequential
from keras.layers import Dense
from keras.optimizer_v2.adam import Adam
from keras.optimizer_experimental.adadelta import optimizer
from sklearn.datasets import load_iris
from sklearn.preprocessing import OneHotEncoder
from sklearn.model_selection import train_test_split

data = load_iris()

features = data.data
labels = data.target.reshape(-1, 1)

encoder = OneHotEncoder()
target = encoder.fit_transform(labels).toarray()

feature_train, feature_test, target_train, target_test = train_test_split(features, target, test_size=0.3)

from keras.optimizer_v2.adam import Adam
from keras.optimizer_experimental.adadelta import optimizer
model = Sequential()
model.add(Dense(10, input_dim=4, activation="sigmoid"))
model.add(Dense(3, activation="softmax"))

model.compile(optimizer=Adam(learning_rate=0.001), loss="categorical_crossentropy", metrics=["accuracy"])

model.fit(feature_train, target_train, batch_size=20, epochs=1000, verbose=2)

result = model.evaluate(feature_test, target_test, use_multiprocessing=True)
print(result)